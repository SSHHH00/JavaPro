public class Ex04_02 {

	public static void main(String[] args) {
			// [for문] 구구단 출력 - (가로로 출력)
			// [for문] 구구단 출력 - (세로로 출력)   
	        for (int i = 1; i <= 9; i++) {
	         for (int d = 2; d <= 9; d++) {  // 단
	            System.out.printf("%d*%d=%2d ", d, i, d*i);
	         } // for d
	         System.out.println();
	      } // for i   

	        
	        
	        
	        
	}	
	
	

}
 


